
# 门禁卡IC卡扇区 十六进制转化十进制
转换方法：
 先调换IC卡序列号顺序 从原序列号末端开始，每次取2位，然后按顺序排列。
 然后把新的组合转换成10进制
 如果最后的结果不足10位，则要在前面补0，补够10位。
示例：
 IC卡序列号为：1A5221B4
 先排序结果为：B421521A
 转换为十进制：3022082586
 由于结果为10位，所以不必要补0。
 
 # ruby方法
 "B421521A".to_i(16)
 => 3022082586

