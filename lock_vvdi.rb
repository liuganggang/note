芯片有固定码;滚动码;加密码3种类型.
固定码:代码固定不变并且由数字与英文字母组成(当启动引擎后,数据不会变动).
滚动码:每个钥匙具有不同的电子代码.但是每次使用钥匙启动车辆引擎后,代码就会被更改.更改代码的程只 有芯片控制器生产商才知道,并且很难通过读取钥匙芯片的记忆进行破解.
加密码:加密代码用于最新的芯片和芯片控制器(采用双向数据加密).它配备有内部程序算法,用于对每次加密的信息进行解密.
